  static SvgPicture svgAssets(
    String name, {
    Color? color,
    double? width,
    double? height,
    BoxFit? fit,
    BlendMode? blendMode,
  }) {
    final size = _svgImageSize[name];
    var w = width;
    var h = height;
    if (size != null) {
      w = width ?? size[0];
      h = height ?? size[1];
    }
    return SvgPicture.asset(
      name,
      colorBlendMode: blendMode ?? BlendMode.srcIn,
      color: color,
      width: w,
      height: h,
      fit: fit ?? BoxFit.none,
    );
  }

  static final Map<String, List<double>> _svgImageSize = {};
}

Widget svgIcon({
  required double h,
  required double w,
  required String image,
  BoxFit fit = BoxFit.contain,
  Color? color,
}) {
  return SizedBox(
    height: h,
    width: w,
    child: SvgPicture.asset(
      image,
      fit: fit,
      color: color,
    ),
  );
}
